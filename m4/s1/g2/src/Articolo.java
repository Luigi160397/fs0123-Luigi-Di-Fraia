
public class Articolo {
	String codiceArticolo;
	String descrizioneArticolo;
	double prezzo;
	int numPezzi;
	
	public Articolo (String codiceArticolo, String descrizioneArticolo, double prezzo, int numPezzi) {
		this.codiceArticolo = codiceArticolo;
		this.descrizioneArticolo = descrizioneArticolo;
		this.prezzo = prezzo;
		this.numPezzi = numPezzi;
	}
}
